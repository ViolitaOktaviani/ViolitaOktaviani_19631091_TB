    private void ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ubahActionPerformed
        try {
            // TODO add your handling code here:
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/penjualanbuku","root","");
            cn.createStatement().executeUpdate("update transaksi set id_buku ='"+idBuku.getText()+"',nama_pembeli ='"+namaPembeli.getText()+"',nama_buku ='"+namaBuku.getText()+"',harga ='"+namaHarga.getText()+"' where id_transaksi = '"+idTransaksi.getText()+"'");
            database();
            Clear();
            JOptionPane.showMessageDialog(null, "Data Berhasil Di Ubah");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Data Gagal Di Ubah");
        }
    }//GEN-LAST:event_ubahActionPerformed

    
